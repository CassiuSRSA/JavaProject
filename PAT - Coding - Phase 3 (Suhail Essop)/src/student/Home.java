    public boolean isEmptyStudent() {
        if (newStudentName.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Student name is missing");
            return false;
        }
        if (newStudentDob.getDate() == null) {
            JOptionPane.showMessageDialog(this, "Student DOB is missing");
            return false;
        }
        if (newStudentDob.getDate().compareTo(new Date()) > 0) {
            JOptionPane.showMessageDialog(this, "No time traveling students allowed");
            return false;
        }
        if (newStudentEmail.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Student email is missing");
            return false;
        }
        if (!newStudentEmail.getText().matches("^.+@.+\\..+$")) {
            JOptionPane.showMessageDialog(this, "Invalid email address");
            return false;
        }
        if (newStudentPhone.getText().length() >= 15) {
            JOptionPane.showMessageDialog(this, "Invalid telephone number");
            return false;
        }
        if (newStudentFather.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Student father name is missing");
            return false;
        }
        if (newStudentMother.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Student mother name is missing");
            return false;
        }
        if (newStudentAddress1.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Address line 1 is missing");
            return false;
        }
        if (newStudentAddress2.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Address line 2 is missing");
            return false;
        }
        if (imagePath == null) {
            JOptionPane.showMessageDialog(this, "Please add your image");
            return false;
        }
        return true;
    }

