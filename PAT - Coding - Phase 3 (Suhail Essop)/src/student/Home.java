    public boolean isNumeric(String s) {

        try {
            double d = Double.parseDouble(s);
            if (d >= 0.0 && d <= 100.0) {
                return true;
            } else {
                JOptionPane.showMessageDialog(this, "Please enter a valid value, between 0 and 100");
                return false;
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "" + e);
        }
        return false;
    }

