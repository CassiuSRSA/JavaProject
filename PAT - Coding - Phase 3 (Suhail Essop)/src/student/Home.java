    private void jButton13ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton13ActionPerformed
        if (subjectStudentId.getText().isEmpty() || subjectSemester.getItemCount() == 0) {
            JOptionPane.showMessageDialog(this, "Please select student and semeseter");
        } else {
            int id = subject.getLatestId();
            int sid = Integer.parseInt(subjectStudentId.getText());
            int semesterNo = Integer.parseInt(subjectSemester.getSelectedItem().toString());
            String subject1 = newSubject1.getSelectedItem().toString();
            String subject2 = newSubject2.getSelectedItem().toString();
            String subject3 = newSubject3.getSelectedItem().toString();
            String subject4 = newSubject4.getSelectedItem().toString();
            String subject5 = newSubject5.getSelectedItem().toString();
            if (subject.isSemesterExist(sid, semesterNo)) {
                JOptionPane.showMessageDialog(this, "This student has already taken semester " + semesterNo);
            } else {
                if (subject.isSubjectExist(sid, "subject1", subject1)) {
                    JOptionPane.showMessageDialog(this, "This student has already taken " + subject1 + " subject");
                } else {
                    if (subject.isSubjectExist(sid, "subject2", subject2)) {
                        JOptionPane.showMessageDialog(this, "This student has already taken " + subject2 + " subject");
                    } else {
                        if (subject.isSubjectExist(sid, "subject3", subject3)) {
                            JOptionPane.showMessageDialog(this, "This student has already taken " + subject3 + " subject");
                        } else {
                            if (subject.isSubjectExist(sid, "subject4", subject4)) {
                                JOptionPane.showMessageDialog(this, "This student has already taken " + subject4 + " subject");
                            } else {
                                if (subject.isSubjectExist(sid, "subject5", subject5)) {
                                    JOptionPane.showMessageDialog(this, "This student has already taken " + subject5 + " subject");
                                } else {
                                    subject.insert(id, sid, semesterNo, subject1, subject2, subject3, subject4, subject5);
                                    jTable2.setModel(new DefaultTableModel(null, new Object[]{"ID", "Student ID", "Semester", "Subject 1", "Subject 2", "Subject 3", "Subject 4", "Subject 5"}));
                                    subject.getSubjectValue(jTable2, "");
                                    clearSubject();
                                }
                            }
                        }
                    }
                }
            }

        }

    }//GEN-LAST:event_jButton13ActionPerformed

